// Declarativa , se declara la funcion con la palarba FUNCION, se le debe dar un nombre ene la funcion se abre () y {} la funcion va aguadar valores, con el nombre se guarda un espacion en memoria 

function miFuncion () {
    return 3; 
}

miFuncion (); //=> llamado funcion declarativa

// Expresion - la variables pueden guardar otro tipo de valaroes como funciones
// La funcion tambien necesitan paramenrtros par que los parametros actuen de cierta manera y pueda hacer algo con ellos ej: a + b 
// Funciones anonimas = por que es una varible que contiene una funcion 

var miFuncion = function (a,b) {
    return a + b; 
}

miFuncion (); // => llamdo funcion expresiva }

